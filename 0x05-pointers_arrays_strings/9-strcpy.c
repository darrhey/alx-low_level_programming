#include <unistd.h>
#include <string.h>
#include <stdio.h>
#include "main.h"
/**
 * strcpy - copies the string pointed to by src to dest
 * @dest: where string will be copied
 * @src: string to be copied
 * Return: return the dest
 */

char *_strcpy(char *dest, char *src)
{
strcpy(dest, src);
return (dest);
}
